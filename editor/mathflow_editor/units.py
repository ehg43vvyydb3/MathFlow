"""책의 단원(대단원-중단원) 구성.

목차(6~8쪽)를 읽어 검증한 페이지 범위다. 목차상 각 소단원의 "01" 페이지
번호를 기준으로 다음 단원의 시작 바로 전까지를 끝 페이지로 잡되, 그
경계에 다음 단원 표지(장 도입부 그림+제목만 있는 페이지)가 끼어 있으면
표지가 속한 다음 단원 쪽으로 옮긴다 — 예: 33쪽은 목차 페이지 번호로는
"1 평면좌표"(10쪽 시작) 범위에 들어가지만 실제로는 "2 직선의 방정식"
표지라서 Ⅰ-2에 포함시켰다 (사용자 확인, 2026-07-11).

이 표지 오귀속 버그가 Ⅰ-2 이후 경계마다 전부 반복되고 있었다 (2026-07-14,
PDF 원본 렌더링해서 페이지별로 직접 눈으로 확인). 각 소단원은 "Ⅰ/Ⅱ/Ⅲ +
대단원명 + 하위 목차(현재 소단원 강조 표시)" 형태의 표지가 자신의 첫
페이지로 반복 등장하는데, 이전 표까지는 그 표지를 전부 "이전" 단원의
마지막 페이지로 잘못 셌다. 실측 결과:
  - 65쪽 = Ⅰ-3 표지 (이전 표는 Ⅰ-2 마지막 페이지로 오분류)
  - 97쪽 = Ⅰ-4 표지 (96쪽은 "쉬어가기" 필러 페이지, Ⅰ-3에 그대로 귀속)
  - 119쪽 = Ⅱ 대단원 표지(Ⅱ-1 강조) (이전 표는 Ⅰ-4 마지막 페이지로 오분류)
  - 137쪽 = Ⅱ-2 표지
  - 165쪽 = Ⅱ-3 표지 (164쪽은 쉬어가기 필러, Ⅱ-2에 귀속)
  - 205쪽 = Ⅲ 대단원 표지(Ⅲ-1 강조) (204쪽은 쉬어가기 필러, Ⅱ-3에 귀속)
  - 247쪽 = Ⅲ-2 표지
  - 275쪽 = Ⅲ-3 표지 (274쪽은 쉬어가기 필러, Ⅲ-2에 귀속)
  - 291쪽은 실제로 Ⅲ-3의 마지막 연습문제 페이지, 292쪽부터 "빠른 정답
    찾기"(부록)라 이 뒤로는 표지 오귀속 문제가 없다 — Ⅲ-3의 끝은 안 밀림.
결과적으로 Ⅰ-1 구간과 Ⅲ-3의 끝(291)만 그대로고, 나머지 모든 시작/끝
페이지가 한 쪽씩 앞으로 당겨졌다. 다른 경계도 이런 표지 페이지가 끼어
있을 수 있으니 편집하다 발견하면 알려줄 것.
"""
from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class Unit:
    id: str
    title: str
    start_page: int
    end_page: int

    @property
    def page_range(self) -> range:
        return range(self.start_page, self.end_page + 1)


UNITS: list[Unit] = [
    Unit("I-1", "Ⅰ-1. 평면좌표", 10, 32),
    Unit("I-2", "Ⅰ-2. 직선의 방정식", 33, 64),
    Unit("I-3", "Ⅰ-3. 원의 방정식", 65, 96),
    Unit("I-4", "Ⅰ-4. 도형의 이동", 97, 118),
    Unit("II-1", "Ⅱ-1. 집합의 뜻과 포함 관계", 119, 136),
    Unit("II-2", "Ⅱ-2. 집합의 연산", 137, 164),
    Unit("II-3", "Ⅱ-3. 명제", 165, 204),
    Unit("III-1", "Ⅲ-1. 함수", 205, 246),
    Unit("III-2", "Ⅲ-2. 유리함수", 247, 274),
    Unit("III-3", "Ⅲ-3. 무리함수", 275, 291),
]

UNITS_BY_ID: dict[str, Unit] = {u.id: u for u in UNITS}


def unit_containing(page_number: int) -> Unit | None:
    for u in UNITS:
        if u.start_page <= page_number <= u.end_page:
            return u
    return None
